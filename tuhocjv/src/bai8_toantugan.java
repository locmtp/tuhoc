public class bai8_toantugan {
    public static void main(String[] args) {
        // Gán cộng
        int x=5;
        x+=5; //x=x+5
        System.out.println("X="+x);
        //gán trừ
        int x2=10;
        x2-=6; //x2=x2-6
        System.out.println("X2="+x2);
        // tương tự với nhân chia

        int a=6;
        a+=3;
        System.out.println("a="+a);
        a-=5;
        System.out.println("a="+a);
        a*=3;
        System.out.println("a="+a);
        a%=5;
        System.out.println("a="+a);

    }
}
